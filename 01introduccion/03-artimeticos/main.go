package main

import "fmt"

func main() {
	a := 20
	b := 10

	// Suma
	result := a + b
	fmt.Println("Suma:", result)

	// Resta
	result = a - b
	fmt.Println("Resta:", result)

	// Multiplicacion
	result = a * b
	fmt.Println("Multiplicación:", result)

	// División
	result = a / b
	fmt.Println("División:", result)

	// Módulo
	result = a % b
	fmt.Println("Módulo:", result)
}
